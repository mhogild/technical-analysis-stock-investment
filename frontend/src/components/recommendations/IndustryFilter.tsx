function getIcon(id: string, type: "stock" | "etf") {
  const normalizedId = id.toLowerCase().replace(/[^a-z]/g, "");
  if (type === "stock") {
    return industryIcons[normalizedId] || industryIcons.industrials;
  }
  return etfCategoryIcons[normalizedId] || etfCategoryIcons.sector;
}

